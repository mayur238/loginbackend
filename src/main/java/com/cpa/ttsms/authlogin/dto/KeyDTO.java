package com.cpa.ttsms.authlogin.dto;

public class KeyDTO {

	private String key;

	/**
	 * 
	 */
	public KeyDTO() {
		super();
	}

	/**
	 * @param key
	 */
	public KeyDTO(String key) {
		super();
		this.key = key;
	}

	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}

	/**
	 * @param key the key to set
	 */
	public void setKey(String key) {
		this.key = key;
	}

}
